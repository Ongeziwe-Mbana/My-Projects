public class HelloWorld {
    public static void main(String[] args){
        int x = 11;

        switch (x) {

            case 1 :

                System.out.print("1");

            case 11 :

                System.out.print("2");

            case 111 :

                System.out.print("3");

            case 1111 :

                System.out.print("4");

            default :

                System.out.print("!");

        }
    }
}